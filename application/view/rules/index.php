        <p><strong>No Spinning</strong> - it is a spin if the rod rotates more than 360 ̊ before striking the ball OR after striking the ball.</p>


        <p><strong>Kick Off</strong> - Reds start with the kick off. Subsequent kick-offs are to the team conceding the most recent 
            goal. Play starts with the ball stationary on the midfield (5-bar). Ensure your 
            opponent is ready to play, and the ball must touch two 5-bar players before 
            it can be played forward in a pass or shot.</p>



        <p><strong>Dead Ball</strong> - A ball on the table is declared ‘dead’ if it cannot be reached 
            by any player figure. If the ball is dead between opposing 5-rods, play is 
            re-started with a new midfield kick-off to the team which conceded the 
            last goal. If the ball goes dead behind the midfield rods, it is re-started in 
            the nearest defensive area. Ensure your opponent is ready to re-start play. 
            The ball must touch two players before being passed or shot. If a team 
            deliberately makes the ball dead, possession is forfeited to the opposing 
            team for a 5-bar kick-off.</p>


        <p><strong>Ball off table</strong> - The ball is ‘out’ if it leaves the playing area, defined as 
            the area inside the cabinet or directly above it, including the edges of the 
            table. If the ball hits scoring markers, ashtrays etc and rebounds into play 
            it is dead. Play is re-started in defence by the team which did not initiate 
            the most recent shot (i.e. if the ball is shot by the attacking team and is 
            deflected out of the table by the defending team, it goes to the defence of 
            the defending team). Ensure your opponent is ready to re-start play. The ball 
            must touch two players before being passed or shot.</p>

        <p><strong>Bounce Outs</strong> - a ball which enters the goal and bounces out counts 
            as a goal. Play is re-started with a new midfield kick-off to the team which 
            conceded that goal.</p>

